def main():
    fake = Faker("ru_RU")
    for i in range(1, 11):
        s_random_number = random.randint(3, 18)
        a_random_number = random.randint(3, 18)
        e_random_number = random.randint(3, 18)
        i_random_number = random.randint(3, 18)
        l_random_number = random.randint(3, 18)

        random_skill_1, random_skill_2, random_skill_3 = random.sample(SKILLS, 3)

        for letter, runic in RUNIC_SYMBOLS.items():
            random_skill_1 = random_skill_1.replace(letter, runic)
            random_skill_2 = random_skill_2.replace(letter, runic)
            random_skill_3 = random_skill_3.replace(letter, runic)

        context = {
            "first_name": fake.first_name(),
            "last_name": fake.last_name(),
            "job": fake.job(),
            "town": fake.city(),
            "strength": s_random_number,
            "agility": a_random_number,
            "endurance": e_random_number,
            "intelligence": i_random_number,
            "luck": l_random_number,
            "skill_1": random_skill_1,
            "skill_2": random_skill_2,
            "skill_3": random_skill_3
        }

        output_svgname = "output/svg/result_{}.svg".format(i)
        file_operations.render_template("src/charsheet.svg", output_svgname, context)

if __name__ == '__main__':
    main()